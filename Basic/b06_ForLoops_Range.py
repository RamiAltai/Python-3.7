# for loops on ranges!

# from 0 to an ending point
# range(end)
for x in range(10):
    print(x)


# from a starting point to an ending point
# range(start, end)
for y in range(100, 105):
    print(y)

# start, end and delta "in steps of delta instead of 1"
# range(100, 200, 10)
for z in range(100, 200, 10):
    print(z)